package by.sportliner.lk.core.support.jpa;

/**
 * Comparison operator.
 */
public enum ComparisonOperator {

    /**
     * Less than.
     */
    LESS,

    /**
     * Less than or equal.
     */
    LESS_OR_EQUAL,

    /**
     * Equal.
     */
    EQUAL,

    /**
     * Greater than or equal.
     */
    GREATER_OR_EQUAL,

    /**
     * Greater than.
     */
    GREATER

}
